	<h1>Gente</h1>
	<p><b>Nota:</b> Esta es una lista incompleta, un gran número de gente ha
	  contribuído al proyecto. </p>
	
	<h5>Ian Clarke</h5>
	<p>
	  Freenet está basada en el artículo de Ian "Un sistema de almacenamiento y recuperación de información distribuído descentralizado"
	  ("A Distributed Decentralised Information
	  Storage and Retrieval System").  Ian comenzó el Proyecto Freenet alrededor de July de
	  1999, y continúa coordinando el projecto.  En su trabajo diario, Ian es el
	  fundador y CEO de <a href="http://sensearray.com/">SenseArray</a>.
	</p>
	
	<h5>Matthew Toseland</h5>
	<p>
	  Matthew ha estado trabajando en Freenet desde antes de la versión 0.5.  Su trabajo
	  y el de otros ha resultado en una dramática mejora del funcionamiento
	  y estabilidad de la red.
	</p>

	<h5>Oskar Sandberg</h5>
	<p>
	  Oskar también fué uno de los primeros colaboradores al proyecto Freenet, y
	  continúa teniendo un rol central en el desarrollo en curso.
	</p>

	<h5>Florent Daigni&egrave;re</h5>
	<p>
	  Florent ha sido nuestro administrador de sistemas casi desde 0.7 fué iniciada,
	  y ha ayudado en otros temas como el instalador actual.
	</p>

	<h5>Scott Miller</h5>
	<p>
	  Scott es responsable de la implementación de muchos de los elementos criptográficos
	  dentro de Freenet.
	</p>

	<h5>Steven Starr</h5>
	<p>
	  Steven ayuda en la administración del Proyecto Freenet Inc, y es consultor    
	  del proyecto en materia de negocios y publicidad.
	</p>

	<h5>Michael Rogers</h5>
	<p>
	  Michael ha contribuído mayormente con detalladas simulaciones como parte del Google 
	  Summer of Code. Ha sido de mucha ayuda en el diseño de la 
	  <a href="http://wiki.freenetproject.org/NewTransportLayer">nueva
	    capa de transporte</a>.
	</p>

	<h5>Dave Baker</h5>
	<p>
	  La principal contribución de Daveha sido <a href="freemail.html">Freemail</a>, 
	  su proyecto Summer of Code de construir un sistema de email-sobre-freenet funcional, así también como  
	  alguna depuración y trabajo central en varios lugares.
	</p>

	<h5>Robert Hailey</h5>
	<p>
	  Robert ha ayudado a mejorar la velocidad y seguridad de Freenet encontrando dos errores
	  <b>grandes</b>, y recientemente ha contribuído con algo de código
	</p>

	<h5>David Sowder</h5>
	<p>
	  David (Zothar) ha ayudado al proyecto Freenet cuando su tiempo se lo permite,
	  incluyendo configuración, estadísticas y administración de pares (peers) vía FCP,
	  las páginas de estadísticas de FProxy y los Mensajes de Nodo de  Nodo 2  (N2NM/N2NTMs).
	</p>

	<p>
	  Y <b>cientos de otros</b>, quienes han solicitado no figurar aquí, quienes 
	  prefieren mantenerse anónimos, o quienes no tenemos cerca para agradecerles. 
	  Sin mencionar los miles de usuarios, testers,
	  y <a href="sponsors.html">donantes</a>!
	</p>

