      <h2>Réponses philosophiques</h2>
      <p><b id="what">Qu'est-ce que Freenet ?</b><br/>
	Freenet est un logiciel libre et gratuit conçu pour assurer la vraie liberté
	de communication sur Internet. Tout le monde peut publier et lire de l'information
	avec un anonymat complet. Personne ne contrôle Freenet, pas même ses créateurs,
	ce qui veut dire que le système n'est pas vulnérable aux manipulations, ou à 
	un arrêt du réseau. Freenet est aussi très efficace dans sa façon de gérer
	l'information, puisqu'il remplace dynamiquement les contenus suivant
	la demande. Pour plus d'informations, se référer à <a href="whatis.html">Freenet ?</a>.
	</p>
	
      <p>
	<b id="who">Qui est derrière Freenet?</b><br/>
	Freenet est né à partir du concept de système permettant une publication anonyme, crééé
	par Ian Clarke alors qu'il était étudiant à l'Université d'Édimbourg, en Ecosse. Depuis
	lors, de nombreuses personnes ont contribué à réaliser la proposition de Ian.
      </p>

      <p>
	<b id="trust">Si les auteurs sont anonymes, comment croire l'information ?</b><br/>
	Le signement par chiffrement de l'information permet de prouver la paternité d'un document&nbsp;;
	cette technique est souvent utilisée pour authentifier la paternité des emails. De plus,
	vous pouvez signer l'information tout en restant anonyme, et ainsi avoir une "personnalité"
	anonyme. Vous pouvez prouvez que vous avez écrit différentes informations sur Freenet,
	sans pour autant révéler votre identité. Vous pouvez ainsi vous construire une réputation anonyme
	quant à votre sérieux.
     </p>

      <p>
	<b id="donate-bw">Dois-je donner de l'espace disque ou du bande passante ?</b><br/>
	Vous ne donnez rien dans le sens où vous perdez de l'espace disque ou de la
	bande passante&nbsp;; mais vous ne partagez pas vraiment non plus (au moins pas
	de la même façon qu'avec les applications de partage de fichier). C'est plus
	comme si vous ajoutiez quelque chose aux ressources commune de Freenet.
     </p>

      <p>
	<b id="leech">Je n'ai pas besoin de donner quoi que ce soit quand j'utilise l'application de partage de fichier X, et je peux "leecher" plus.</b><br/>
	Pouvez-vous le faire anonymement ? Freenet est conçu pour l'anonymat d'abord, les performances viennent en seconde position.
      </p>

      <p>
	<b id="donate-more-bw">Tous mes amis ont donné peu d'espace disque et de bande passante. Pourquoi devrais-je donner plus ?</b><br/>
      	Si vous vous contentez de ce que vous avez, alors non. Mais si
	vous voulez plus, vous devriez considérer de donner plus et de faire
	tourner votre nœud 24/7 si possible, et demander à vos amis de faire
	de même.
      </p>

      <p>
	<b id="donate-lot">Si je donne plus, est-ce que les performances vont s'accroitre sensiblement ?</b><br/>
	C'est sûr, mais pour une amélioration encore plus grande, nous avons besoin que
	les utilisateurs commencent à penser comme vous. La bande passante compte plus
	que l'espace disque.
      </p>

      <p>
	<b id="legal">Freenet est-il légal ?</b><br/>
        Nous ne sommes au courant d'aucune poursuite judiciaire suite à l'utilisation de Freenet.
	Certaines personnes affirment que la loi <a href="https://en.wikipedia.org/wiki/DADVSI">DADVSI</a>
	(lien en anglais) rend Freenet illégal en France&nbsp;; d'autres nous ont parlé de la
	loi allemande à propos de la rétention de données, qui exige des systèmes anonymes
	qu'ils conservent suffisamment de données pour tracer les requêtes web, ce qui pourrait,
	ou non, s'appliquer à nous. À l'échelle européenne, la loi 
	<a href="http://www.fsfeurope.org/projects/ipred2/ipred2.html">IPRED2</a> (lien en anglais)
	pourrait avoir de vastes effets, y compris sur Freenet, si elle passe... ou peut-être que non.
	Toutes ces choses peuvent avoir différents effets (suivant comment la législation est 
	appliquée) et devront faire l'objet d'une jurisprudence. Vous pouvez lire les conseils
	pour les développeurs d'application pair à pair de l'EFF (centrés sur les États-Unis)
	<a href="https://www.eff.org/wp/iaal-what-peer-peer-developers-need-know-about-copyright-law">ici</a>
	(lien en anglais). Si vous avez besoin de conseils juridiques, parlez à un avocat.
	Vous pouvez également lire la prochaine section, spécialement si vous êtes en Chine&nbsp;
	peut-être que les autorités bloquent notre protocole parce qu'elles ne nous aiment pas !
      </p>

      <p>
	<b id="blocked">Freenet est-il bloqué par des pares-feu nationaux ?</b><br/>
	Le pare-feu national Chinois (le Bouclier Doré) a bloqué notre site web pendant
	de nombreuses année, ainsi que la version 0.5 du protocole selon des observations
	datant de 2005. Cela suggère que la Chine ne nous aime pas, donc soyez prudent si
	vous faîtes tourner Freenet en Chine. D'autres pays (comme la France) sont connus pour
	être hostile aux réseaux pair à pair, et pourraient obliger les FAI (Fournisseur d'Accès
	à Internet) à bloquer le trafic pair à pair (même si à l'heure actuelle Freenet fonctionne
	parfaitement bien en France, et ce n'est pas nos nombreux utilisateurs Français qui diront
	le contraire !). Freenet 0.7 possède certaines défenses minimales contre le blocage&nbsp;;
	le protocole est relativement difficile à identifier (nous le rendrons encore plus difficile
	dans le future), et le mode <a href="https://wiki.freenetproject.org/DarkNet">darknet</a>
	(dans lequel vous ne vous connectez qu'à vos amis) rend le harvesting (collecte d'IPs) automatique
	et le blocage des nœuds Freenet très difficile.
      </p>
      
      <p>
	<b id="trouble">Puis-je être inquiété si je fais tourner un nœud ?</b><br/>
	Ce sujet est lié à <a href="#legal">"Freenet est-il légal ?"</a>.
	Nous avons fait tout ce que nous pouvions pour rendre extrêmement
	difficile à n'importe quel système judiciaire sensé de justifier de punir
	quelqu'un pour avoir fait tourné un nœud Freenet, et il y a peu de
	précédent de telles actions dans les pays développés actuels. De nombreux
	systèmes judiciaires reconnaissent l'importance de la liberté d'expression,
	qui est le principal but de Freenet. Ceci étant dit, il y a un risque
	en faisant quelque chose avec laquelle votre gouvernement pourrait ne pas
	être d'accord&nbsp;; vous devriez prendre une décision réfléchie pour savoir
	si vous êtes prêt à prendre ce risque. De plus, votre FAI (Fournisseur d'Accès à Internet)
	pourrait avoir un problème avec Freenet. Au moins un fournisseur de solution d'hébergement
	est connu pour avoir banni Freenet (ainsi que Tor et autres) de leurs serveurs&nbsp;;
	merci de lire les termes et conditions de votre offre afin de vous assurez
	que vous avez le droit d'exécuter Freenet.
      </p>

      <p>
	<b id="copyright">Qu'en est-il du droit d'auteur ?</b><br/>
	Il y a d'excellentes opinions à ce sujet sur la page <a href="/philosophy.html">Philosophy</a>.
	Les lois spécifiques aux droits d'auteurs pourraient être un problème, merci de lire
	<a href="#legal">Freenet est-il légal?</a> et <a href="#blocked">Freenet est-il bloqué par des pares-feu nationaux ?</a>.
      </p>

      <p>
	<b id="childporn">Qu'en est-il de la pornographie infantile, des contenus choquants ou offensants, ou du terrorisme ?</b><br/>
	Bien que la plupart des gens aimeraient que la pornographie infantile
	et le terrorisme n'existe pas, l'humanité ne devrait pas être privée
	de la liberté de communiqué simplement parce qu'un très petit nombre
	de personnes pourraient l'utiliser à d'autres fins.
      </p>


      <p>
	<b id="offensive">Je ne veux pas que mon nœud soit utilisé pour héberger de la pornographie infantile, des contenus choquants ou offensants, ou du terrorisme. Que dois-je faire ?</b><br/>
	Le véritable test pour ceux qui affirment croire en la liberté
	d'expression est de savoir s'ils tolèrent un discours avec lequel ils
	sont en désaccord, ou qu'ils trouvent répugnant. Si vous n'êtes pas
	d'accord avec cela, vous ne devriez pas faire tourner de nœud Freenet.
	Il y a autre chose que vous pouvez faire. Puisque le contenu de Freenet est
	disponible tant qu'il est populaire, vous pouvez aider à limiter la
	popularité de n'importe quelle information que vous n'aimez pas. Par exemple,
	si vous ne voulez pas qu'un fichier se répande, vous ne devriez pas
	le télécharger, et faire passer le messages à tous ceux que vous connaissez.
	Cependant, il vous faut garder à l'esprit que Freenet n'est pas conçu pour
	rendre possible la communication entre les personnes uniquement si
	un nombre suffisant de personnes est d'accord avec cette communication.
	Freenet est conçu pour rendre la communication possible même s'il n'y a
	qu'un éditeur et qu'un lecteur, ce qui est presque déjà le cas avec la version
	actuelle de Freenet. 
      </p>

      <p>
	<b id="export">How about encryption export restrictions?</b><br/>
      	Le projet Freenet a averti les autorités des États Unis (puisque les fichiers sont
	hébergés sur le territoire américain) qu'il fait usage d'algorithmes de chiffrements.
	Tant que votre pays n'interdit pas l'utilisation de chiffrement, c'est bon.    
     </p>

      <p>
	<b id="whatelse">Je n'ai rien à cacher et je n'ai pas besoin d'anonymat. Y a-t-il autre chose que Freenet puisse offrir ?</b><br/>
	Oui, en fait même sans garantir l'anonymat, Freenet est déjà
	très utile, grâce à sa façon unique de gérer la distribution
	de contenus et la charge d'information. Plus simplement,
	cela signifie que vous pouvez publier un site sans vous
	préoccuper de sa taille et sans avoir à y intégrer la bannière
	publicitaire de quelqu'un d'autre. Bien qu'il soit peu probable
	que les sites Freenet (freesites) soient un jour chargés plus
	rapidement que les sites web "normaux", le réseau s'adapte mieux à
	un pic soudain de visiteurs (ce qui est souvent le cas quand le lien de
	sites quasi inconnus se retrouvent sur un gros site) et des taux de
	transferts élevés pour des gros fichiers est aussi envisageable. Ne vous
	attendez simplement pas à une latence faible.
      </p>
      
      <h2>Réponses techniques</h2>

      <p>
	<b id="how">Comment utiliser ce logiciel ? Je l'ai téléchargé, mais quand il s'exécute, il n'y a pas d'interface graphique.</b><br/>
	Fred (the Freenet REference Daemon - démon Freenet de référence) est éxécuté en tant que démon, ou service, en tâche de fond. Vous pouvez communiquer avec via un client Freenet. FProxy, par exemple, est un client intégré qui vous permet de communiquer avec Freenet à travers un navigateur internet.
	Lors de l'installation de Freenet, un raccourci "Browse Freenet" devrait avoir été créé sur le bureau et/ou dans le menu démarrer, ainsi qu'un icône dans la zone de notification (lapin Freenet) avec un item "Launch Freenet".
	Si rien de tout cela ne fonctionne, visitez la page <a href="http://127.0.0.1:8888/" rel="nofollow">http://127.0.0.1:8888/</a> avec votre navigateur. Cliquez sur quelques liens situés dans le paneau "Freesite subscriptions" pour visiter quelques freesites d'indexation populaires.
      </p>

      <p>
	Si vous cherchez des applications qui s'exécutent au-dessus de Freenet et fournissent différentes interface et fonctionnalités, merci de vous référer à la page <a href="/tools.html">Outils</a>.
      </p>

      <p>
	<b id="slow">Pourquoi Freenet est-il aussi lent ?</b><br/>
	Quand vous installez Freenet pour la première, il sera lent, et vous pourrez
	rencontrer les erreurs "Data Not Found" (Donnée introuvable) ou "Route Not Found"
	(Route introuvable). C'est normal, et Freenet deviendra sensiblement plus rapide 
	au fil du temps. Pour atteindre les meilleures performances possibles, vous devriez
	essayer de faire tourner Freenet aussi longtemps que possible (idéalement, 24h/24).
	C'est pour cette raison que nous installons Freenet en tant que service.
      </p>
      <p>
	Merci de garder à l'esprit qu'une latence élevée est inhérente au principe de Freenet :
	cela peut prendre du temps à charger une page la première fois, même si Freenet est
	capable de vitesses raisonnables (pour un système anonyme !) pour les gros fichiers
	populaires. Vous pouvez également améliorer les performances en utilisant un 
	navigateur autre que celui que vous utilisez habituellement et <a href="#connections">accroître 
	sa limite de connexions</a>. Vous devriez aussi configurer la taille du datastore et la limite de
	bande passante aussi hautes que possible. Mais protéger votre anonymat est coûteux en
	terme de performance. Vous pouvez configurer cela en changeant les niveaux de sécurité dans
	la page Configuration.
      </p>

      <p>
	<b id="search">Peut-on effectuer des recherches sur Freenet ?</b><br/>
	Oui, il y a quelques mécanismes de recherche différents. Pour chercher dans
	les freesites, vous pouvez simplement utiliser le champ de recherche de la
	page d'accueil, ou bien vous rendre sur la page "Chercher sur Freenet" dans le menu
	"Naviguer". Si elle n'existe pas, rendez-vous dans la section Plugins de la page
	de Configuration, et chargez le plugin nommé "Library". De façon alternative,
	Frost et Thaw fournissent aussi des fonctions de recherche de messages et
	de fichiers. Veuillez noter qu'il est bien plus difficile de chercher quelque chose
	sur Freenet que sur les autres réseaux, en raison de son architecture et de sa conception
	qui ont des objectifs différents.
      </p>

      <p><b id="firewall">Comment puis-je faire fonctionner Freenet avec un pare-feu/NAT ?</b><br/>
	Mostly, Freenet should just work with a NAT. However, you should forward the ports
	manually if you can. Click on the <a href="http://127.0.0.1:8888/friends/" rel="nofollow">Friends</a> or <a href="http://127.0.0.1:8888/strangers/">Strangers</a> page. At the bottom you will
	see a list of ports used by the node. You should forward (for UDP) the Darknet FNP 
	and Opennet FNP ports. You may need to look up your router's documentation to figure
	out how to do this. Freenet should have forwarded them itself through <a href="https://en.wikipedia.org/wiki/Universal_Plug_and_Play">Universal Plug and Play</a>,
	but this doesn't always work (and it never works if you don't have the UPnP plugin loaded, or have one router behind another).</p>

      <p>If you have a dyndns address or other domain name pointing to the computer you run 
	your Freenet node on, tell the node about it. Go to <a href="http://127.0.0.1:8888/config/">the config page</a>, 
	and find the option "IP address override". Put your domain name in that box, and apply the settings.</p>

      <p>
	<b id="connection-perm">Ai-je besoin d'une connexion permanente pour faire tourner un nœud ?</b><br/>
	Non, mais c'est préférable. Vous pouvez exécuter le logiciel et le 
	tester sur une connexion "éphémère" (telle que fournie par les configurations
	habituelles des FAI/modem), mais pour que l'ensemble du réseau soit plus
	utile et performant, nous avons besoin d'un maximum de connexion permanente
	(la plupart des connexions par cable ou DSL sont suffisamment "permanentes").
	Une version future de Freenet pourrait tirer un meilleur avantage des
	nœuds "éphémères". 
      </p>

      <p><
	b id="connections">Why does Freenet only download 1 or 2 files at a time?</b><br/>
	De nombreux navigateurs limitent beaucou trop le nombre de connexions simultanées pour pouvoir
	naviguer efficacement sur Freenet (puisque les pages Freenet ont souvent une latence bien plus grandes que
	les pages web). Ce comportement peut être configuré. Par exemple, pour Mozilla Firefox, entrez 
	<span style="font-weight: bold;">about:config</span> dans la barre d'adresse et remplacez les valeurs des 
	otpions suivantes par les valeurs données ici. Tapez <span style="font-weight: bold;">&quot;connections&quot;</span> dans
	le champ "Filter" pour n'afficher que les options pertinentes :</p>

      <code>
	network.http.max-connections 200<br/>
	network.http.max-connections-per-server 200<br/>
	network.http.max-persistent-connections-per-proxy 200<br/>
	network.http.max-persistent-connections-per-server 200<br/>
      </code>
      
      <p>
	Veuillez noter que Mozilla Firefox utilisera plus de connexions
	pour tout type de navigation, y compris pour les sites webs, ce qui
	n'est pas souhaitable d'un point de vue de la congestion du réseau.
	Toute aide pour permettre à mozilla d'adopter un comportement qui s'adapte en
	fonction du site visité serait la bienvenue...
      </p>

      <p>
	<b id="store-perm">Pourquoi Freenet ne peut-il pas stocker les données de façon permanente ?</b><br/>
      	Parce que nous ne trouvons aucune façon de réaliser cela sans compromettre
	les autres objectifs de Freenet. Par exemple, on nous suggère souvent
	que les nœuds ne devraient pas pouvoir effacer les données que le possesseur
	veut garder de façon permanente. Cependant, cela ne fonctionne pas, puisqu'il
	n'y a aucune garantie que même si les données sont toujours stockées dans leur
	nœud, les requêtes pour ces données seront bien routées vers ce nœud.
	Nous avons considéré de nombreuses autres façon pour Freenet de stocker
	les données de façon permanente, mais soit elles ne fonctionneront pas,
	soit elles compromettent un des objectifs principaux de Freenet, ou l'anonymat,
	ou l'extensibilité (passage à l'échelle). Toutefois, si vous réinsérez simplement
	les données régulièrement, ou qu'elles sont régulièrement téléchargées (pas par
	la même personne puisqu'elles seraient mises en cache sur leur nœud ou sur le
	chemin vers leur nœud), elles devraient être disponibles indéfiniment.
      </p>

      <p>
	<b id="why-java">Pourquoi avoir implémenté Freenet en java ?</b><br/>
	Les opinions diffèrent à propos du choix de Java pour l'implémentation
	de référence de Freenet (même entre les développeurs principaux). <a href="/people.html">Ian Clarke</a> 
	et plusieurs autres développeurs sont en faveur de Java, et un choix en faveur de Java a été fait. Même
	si tout le monde était convaincu de changer de langage, réimplémenter le protocole Freenet actuel serait
	une tâche plutôt énorme, et prendrait un temps considérable, alors même que nous avons des ressources
	limitées. Les joutes sur la liste de discussion consacrée au développement à propos du choix du langage
	ne sont pas acceptées. Cependant, si quelqu'un souhaite implémenter Freenet dans un autre langage, nous
	l'encourageons chaleureusement à essayer. Toutefois, ne sous-estimez pas la quantité de travail à fournir. 
      </p>
      
