	<h1>People</h1>
	<p><b>Note:</b> This is an incomplete list, a large number of people have
	  contributed to the project. </p>
	
	<h5>Ian Clarke</h5>
	<p>
	  Freenet is based on Ian's paper "A Distributed Decentralised Information
	  Storage and Retrieval System".  Ian started the Freenet Project around July of
	  1999, and continues to coordinate the project.  In his day job, Ian is the
	  founder and CEO of <a href="http://sensearray.com/">SenseArray</a>.
	</p>
	
	<h5>Matthew Toseland</h5> 

        <p> Matthew has been working on Freenet since before the 0.5
	release.  He has been pushing freenet through obstacles for
	ten years which no sane person would have dared to tackle -
	and who succeeded at solving at least three unsolvable
	problems. Now took up studying CS again at
	Cambridge. Previously a zealous christian who believed that
	god would judge him fairly, now an atheist who fights for the
	future of human civilization.  </p>

	<h5>Oskar Sandberg</h5>
	<p>
	  Oskar was also one of the earliest contributors to the Freenet project, and
	  has made some important theoretical breakthroughs that lead to the beginning of 
	  Freenet 0.7, see the papers page.
	</p>

	<h5>Florent Daigni&egrave;re/nextgens</h5>

	<p> Since 2003, Florent has improved various aspects of the
	  software and performed the project's system
	  administration. A long-term contributor who tends to be
	  proven right in the end and works as full-time security
	  consultant besides taking on the crazy crypto challenges in
	  freenet and mentoring GSoC students on tackling hard crypto
	  tasks.  </p>

<h5>operhiem1</h5>

<p>Really good at integrating the huge number of different libraries and tools which make up freenet and at the same time able to keep cheerful contact with the folks who contribute to freenet. Now provides his skills at writing very clean code and documenting it effectively to a company for paying back his student loan - besides being freenet maintainer and release manager.
</p>

<h5>digger3</h5>

<p>Long-term contributor, statistics-guru, very friendly (when he thinks that being friendly is constructive in the situation), good at quickly solving problems in three days which take months when others tackle - and now finished with his PhD. Moved to teaching collegues at his employer that having a camera in the lunch room is a pretty bad idea.
</p>

<h5>p0s/xor/he-who-cannot-be-named</h5>

<p>The one who actually implemented WoT and who’s taken on the mission on making it work well and fast, despite knowing that scaling distributed spam-protection is a problem which isn’t even solved in the clearnet. The second paid developer. Prefers being paid in BitCoin. Actually works sensible hours and managed to get his stuff together while working on freenet.
</p>

<h5>Bombe</h5>

<p>The demo-scene artist in exile who writes Sone, the first freenet application which looks like it’s from the current millenium, and enables actually working,decentralized, anonymous microblogging in twitter- or facebook-style using the Web of Trust to find new people - and get rid of spammers. He’s pretty good at avoiding distractions by anything which looks like trolling.
</p>

